std::vector<double> sampleStats(std::vector<int>& count)
{
    std::vector<double> res;
    int nfirst = -1;
    int nlast = 256;
    int valueMax = -1;
    int nMax = -1;
    double dSumVal = 0;
    double dSumCount = 0;
    double dCurCount = 0;
    std::vector<double> dCount(256, 0);
    for (int i = 0; i < count.size();i++)
    {
        if (count[i] != 0)
        {
            if (nfirst == -1)
                nfirst = i;
            nlast = i;
            if (valueMax < count[i])
            {
                valueMax = count[i];
                nMax = i;
            }
            dSumVal += count[i] * i;
            dSumCount += count[i];
            
        }
        if (i == 0)
            dCount[i] = count[i];
        else
            dCount[i] = dCount[i - 1] + count[i];
    }
    dSumVal /= dSumCount;
    dSumCount = dCount[dCount.size() - 1];
    double dmiddst = ((int)dSumCount) / 2;
    if(((int)dSumCount) % 2==0)
    { 
        for (int i = 0; i < dCount.size()-1; i++)
        {
            if (dCount[i] == dmiddst)
            {
                dCurCount = (i + i + 1) / 2.0;
                break;
            }
            else if (dCount[i] > dmiddst)
            {
                dCurCount = i;
                break;
            }
        }
    }
    else
    {
        for (int i = 0; i < dCount.size(); i++)
        {
            if (dCount[i] >= dmiddst)
            {
                dCurCount = i;
                break;
            }
        }
    }

    res.push_back(nfirst);
    res.push_back(nlast);
    res.push_back(dSumVal);
    res.push_back(dCurCount);
    res.push_back(nMax);
    return res;
}
